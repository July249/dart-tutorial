void main() {
  // final keyword is used to declare a variable that can be set only once.
  // Once a final variable is set, it can never be changed.
  final name = 'Bob';
  // you can assign type of final variable
  // final String nickname = 'Bobby'; <- this is not mendatory!
  // name = 'Alice'; // Error: A final variable can only be set once.
  print(name);
}
